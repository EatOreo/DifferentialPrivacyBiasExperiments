def score2(gender: str, smoker: bool, age: int, ldl: float, sys: int):
    if age < 40 or age > 69 or ldl < 3 or ldl > 6.9 or sys < 100 or sys > 179:
        return -1

    age = age - (age % 5)
    ldl = ldl - (ldl % 1)
    sys = sys - (sys % 20)

    if gender == 'F' and not smoker:
        return womenNoSmoke[age][ldl][sys]
    if gender == 'M' and not smoker:
        return menNoSmoke[age][ldl][sys]
    if gender == 'F' and smoker:
        return womenSmoke[age][ldl][sys]
    if gender == 'M' and smoker:
        return menSmoke[age][ldl][sys]
    return -1
